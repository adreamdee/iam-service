package io.choerodon.iam.domain.iam.entity;

/**
 * @author wuguokai
 * @author superlee
 */
public class OrganizationE {

    private Long id;

    private String name;

    private String code;

    private Long objectVersionNumber;

    private Boolean enabled;

    private Long userId;

    private String address;

    private String imageUrl;

    public OrganizationE(Long id, String name, String code,
                         Long objectVersionNumber, Boolean enabled, Long userId, String address) {
        this.id = id;
        this.name = name;
        this.objectVersionNumber = objectVersionNumber;
        this.code = code;
        this.enabled = enabled;
        this.userId = userId;
        this.address = address;
    }

    public OrganizationE() {
    }

    public Long getId() {
        return id;
    }

    public OrganizationE setId(Long id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public OrganizationE setName(String name) {
        this.name = name;
        return this;
    }

    public Long getObjectVersionNumber() {
        return objectVersionNumber;
    }

    public OrganizationE setObjectVersionNumber(Long objectVersionNumber) {
        this.objectVersionNumber = objectVersionNumber;
        return this;
    }

    public String getCode() {
        return code;
    }

    public Boolean getEnabled() {
        return enabled;
    }

    public OrganizationE enable() {
        this.enabled = true;
        return this;
    }

    public OrganizationE disable() {
        this.enabled = false;
        return this;
    }

    public Long getUserId() {
        return userId;
    }

    public OrganizationE setUserId(Long userId) {
        this.userId = userId;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public OrganizationE setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public OrganizationE setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
        return this;
    }

    public OrganizationE setCode(String code) {
        this.code = code;
        return this;
    }

    public OrganizationE setEnabled(Boolean enabled) {
        this.enabled = enabled;
        return this;
    }
}
