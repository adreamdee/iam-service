package io.choerodon.iam.domain.iam.entity;

/**
 * @author superlee
 * @data 2018/4/4
 */
public class RolePermissionE {

    private Long id;

    private Long roleId;

    private Long permissionId;

    public RolePermissionE(Long id, Long roleId, Long permissionId) {
        this.id = id;
        this.roleId = roleId;
        this.permissionId = permissionId;
    }

    public Long getId() {
        return id;
    }

    public Long getRoleId() {
        return roleId;
    }

    public Long getPermissionId() {
        return permissionId;
    }
}
